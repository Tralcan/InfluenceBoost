type SidebarContext = {
  open: boolean
  setOpen: (open: boolean) => void
  toggleSidebar: () => void
}

const SidebarContext = React.createContext<SidebarContext | null>(null)

function useSidebar() {
  const context = React.useContext(SidebarContext)
  if (!context) {
    throw new Error("useSidebar must be used within a SidebarProvider.")
  }
  return context
}

const SidebarProvider = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<"div"> & {
    open?: boolean
    onOpenChange?: (open: boolean) => void
  }
>(
  (
    {
      open: openProp,
      onOpenChange: setOpenProp,
      className,
      children,
      ...props
    },
    ref
  ) => {
    const isMobile = useIsMobile()
    const [_open, _setOpen] = React.useState(false)

    // Sync controlled and uncontrolled states.
    const open = openProp !== undefined ? openProp : _open
    const setOpen = (value: boolean) => {
      if (setOpenProp) {
        setOpenProp(value)
      } else {
        _setOpen(value)
      }
    }

    // Default to open on desktop and closed on mobile.
    React.useEffect(() => {
      setOpen(!isMobile)
    }, [isMobile])

    const toggleSidebar = React.useCallback(() => {
      setOpen(!open)
    }, [open])

    React.useEffect(() => {
      const handleKeyDown = (event: KeyboardEvent) => {
        if (event.key === "b" && (event.metaKey || event.ctrlKey)) {
          event.preventDefault()
          toggleSidebar()
        }
      }
      window.addEventListener("keydown", handleKeyDown)
      return () => window.removeEventListener("keydown", handleKeyDown)
    }, [toggleSidebar])

    const contextValue = React.useMemo<SidebarContext>(
      () => ({
        open,
        setOpen,
        toggleSidebar,
      }),
      [open, setOpen, toggleSidebar]
    )

    return (
      <SidebarContext.Provider value={contextValue}>
        <TooltipProvider delayDuration={0}>
          <div ref={ref} className={cn(className)} {...props}>
            {children}
          </div>
        </TooltipProvider>
      </SidebarContext.Provider>
    )
  }
)
SidebarProvider.displayName = "SidebarProvider"


const Sidebar = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<"div">
>(
  (
    {
      className,
      children,
      ...props
    },
    ref
  ) => {
    const { open, setOpen } = useSidebar()
    const isMobile = useIsMobile()
    
    if(isMobile) {
        return (
            <Sheet open={open} onOpenChange={setOpen}>
              <SheetContent
                ref={ref}
                side="left"
                className="w-[240px] bg-sidebar p-0 text-sidebar-foreground [&>button]:hidden"
              >
                <div className="flex h-full w-full flex-col">{children}</div>
              </SheetContent>
            </Sheet>
        )
    }
    
    return (
        <div
         ref={ref}
         data-collapsed={!open}
         className={cn("hidden md:flex flex-col h-full w-[240px] border-r bg-background transition-[width] data-[collapsed=true]:w-14", className)}
         {...props}
        >
          {children}
        </div>
    )
  }
)
Sidebar.displayName = "Sidebar"

const SidebarTrigger = React.forwardRef<
  React.ElementRef<typeof Button>,
  React.ComponentProps<typeof Button>
>(({ className, onClick, ...props }, ref) => {
  const { toggleSidebar } = useSidebar()

  return (
    <Button
        ref={ref}
        data-sidebar="trigger"
        variant="ghost"
        size="icon"
        className={cn("h-8 w-8", className)}
        onClick={(event) => {
          onClick?.(event)
          toggleSidebar()
        }}
        {...props}
      >
        <PanelLeft />
        <span className="sr-only">Toggle Sidebar</span>
      </Button>
  )
})
SidebarTrigger.displayName = "SidebarTrigger"

